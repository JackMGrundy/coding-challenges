/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {TreeNode}
 */
var invertTree = function(root) {
    var helper = function(node) {
        if (node) {
            helper(node.left);
            helper(node.right);
            [node.left, node.right] = [node.right, node.left];
        }
        return;
    }
    helper(root);
    return root;
};