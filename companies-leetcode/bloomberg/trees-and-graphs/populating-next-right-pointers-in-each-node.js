/**
 * // Definition for a Node.
 * function Node(val,left,right,next) {
 *    this.val = val;
 *    this.left = left;
 *    this.right = right;
 *    this.next = next;
 * };
 */
/**
 * @param {Node} root
 * @return {Node}
 */
var connect = function(root) {
  let res = root;
  while (root !== null && root.left !== null) {
      let cur = root;
      while (cur !== null) {
          cur.left.next = cur.right;
          cur.right.next = cur.next === null ? null : cur.next.left;
          cur = cur.next;
      }
      root = root.left;
  }
  
  return res;
};